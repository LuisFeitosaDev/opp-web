-- =====================================================================
-- DEMANDAS (tabela larga: classificação + datas de todas as etapas)
-- =====================================================================

create table demandas (
  cd_demanda integer generated by default as identity primary key,

  -- Classificação
  canal text,
  griffe text,
  grupo text,
  subgrupo text,
  fornecedor text,
  grupo_planejamento text,
  sexo text,
  atributo_1 text,
  atributo_2 text,
  preco_varejo numeric(10,2),
  lente_antecipada boolean not null default false,

  -- Códigos
  cod_1 text,
  cod_2 text,
  cod_3_licenca text,
  sku text,
  data_compra date,

  -- Controle (exclusão é lógica via status = 'EXCLUIDO')
  status text not null default 'ATIVO' check (status in ('ATIVO','ENCERRADO','CANCELADO','EXCLUIDO')),
  etapa_atual text not null default 'MIX',
  ficha_url text,

  -- MIX
  recebimento_mix date,
  selecao_amostras date,
  aprovacao_mix date,

  -- DESENVOLVIMENTO
  desenvolvimento date,
  aprovacao_desenvolvimento date,
  envio_desenvolvimento date,

  -- DESENHO TÉCNICO
  recebimento_dt date,
  aprovacao_dt date,
  comentarios_dt text,

  -- PROTÓTIPO
  solicitacao_prototipo date,
  recebimento_prototipo date,
  aprovacao_prototipo date,
  aprovacao_licenca_proto date,
  comentarios_proto text,

  -- COLOR SAMPLE
  solicitacao_cs date,
  recebimento_foto_cs date,
  recebimento_fisico_cs date,
  aprovacao_cs date,
  aprovacao_licenca_cs date,
  comentarios_cs text,

  -- SET FINAL
  solicitacao_sf date,
  recebimento_foto_sf date,
  recebimento_fisico_sf date,
  aprovacao_licenca_sf date,
  comentarios_sf text,

  -- LICENÇA GERAL
  comentarios_licenca text,

  -- Auditoria
  criado_por uuid references usuarios (id),
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now()
);

create index idx_demandas_status on demandas (status);
create index idx_demandas_etapa on demandas (etapa_atual);
create index idx_demandas_griffe on demandas (griffe);
create index idx_demandas_canal on demandas (canal);
create index idx_demandas_fornecedor on demandas (fornecedor);
create index idx_demandas_criado on demandas (criado_em desc);

-- =====================================================================
-- ARQUIVOS (anexos por demanda — fichas, desenhos, protótipos...)
