-- =====================================================================
-- FUNÇÕES DE APOIO (Auth / permissões / log)
-- =====================================================================

create or replace function fn_usuario_atual() returns uuid
language sql stable security definer set search_path = public as $$
  select id from usuarios where id = auth.uid() and not bloqueado;
$$;

create or replace function fn_is_admin() returns boolean
language sql stable security definer set search_path = public as $$
  select exists (
    select 1 from usuarios where id = auth.uid() and perfil = 'admin' and not bloqueado
  );
$$;

create or replace function fn_tem_permissao(p_tela text, p_editar boolean default false)
returns boolean language sql stable security definer set search_path = public as $$
  select fn_is_admin() or exists (
    select 1 from permissoes p
    join usuarios u on u.id = p.usuario_id
    where p.usuario_id = auth.uid()
      and p.tela_codigo = p_tela
      and not u.bloqueado
      and p.pode_visualizar
      and (not p_editar or p.pode_editar)
  );
$$;

create or replace function fn_registra_transacao(
  p_transacao text, p_item integer default 0,
  p_info_anterior text default '', p_info_atual text default '', p_campo text default ''
) returns void language sql security definer set search_path = public as $$
  insert into log_transacoes (usuario_id, transacao, cd_item_transacao, info_anterior, info_atual, campo_editado)
  values (auth.uid(), p_transacao, coalesce(p_item, 0), p_info_anterior, p_info_atual, p_campo);
$$;

-- =====================================================================
-- REGRA DE NEGÓCIO: cálculo da etapa atual
-- (portada do etapa-calculator do sistema anterior — a data de aprovação
--  mais avançada preenchida define a etapa)
-- =====================================================================

create or replace function fn_calcula_etapa(d demandas) returns text
language sql immutable as $$
  select case
    when d.status = 'ENCERRADO' then 'ENCERRADA'
    when d.aprovacao_licenca_sf is not null or d.recebimento_fisico_sf is not null then 'SET_FINAL'
    when d.aprovacao_cs is not null then 'SET_FINAL'
    when d.aprovacao_prototipo is not null then 'COLOR_SAMPLE'
    when d.aprovacao_dt is not null then 'PROTOTIPO'
    when d.aprovacao_desenvolvimento is not null then 'DESENHO_TECNICO'
    when d.aprovacao_mix is not null then 'DESENVOLVIMENTO'
    else 'MIX'
  end;
$$;

-- =====================================================================
-- TRIGGERS
-- =====================================================================

-- Campos calculados mantidos pelo banco (etapa atual + atualizado_em)
create or replace function trg_calcula_demandas() returns trigger
language plpgsql as $$
begin
  new.etapa_atual := fn_calcula_etapa(new);
  new.atualizado_em := now();
  if tg_op = 'INSERT' and new.criado_por is null then
    new.criado_por := auth.uid();
  end if;
  return new;
end;
$$;

create trigger calcula_demandas
  before insert or update on demandas
  for each row execute function trg_calcula_demandas();

-- Auditoria campo a campo (gravações de serviço, sem usuário, não geram log)
create or replace function trg_audita_demandas() returns trigger
language plpgsql security definer set search_path = public as $$
declare
  v_col text;
  v_old text;
  v_new text;
begin
  if auth.uid() is null then
    return new;
  end if;
  if tg_op = 'INSERT' then
    insert into log_transacoes (usuario_id, transacao, cd_item_transacao)
    values (auth.uid(), 'EdicaoDemanda - Criacao', new.cd_demanda);
    return new;
  end if;
  for v_col in
    select column_name from information_schema.columns
    where table_schema = 'public' and table_name = 'demandas'
      and column_name not in ('atualizado_em', 'criado_em', 'criado_por', 'etapa_atual')
  loop
    execute format('select ($1).%I::text, ($2).%I::text', v_col, v_col) into v_old, v_new using old, new;
    if v_old is distinct from v_new then
      insert into log_transacoes (usuario_id, transacao, cd_item_transacao, info_anterior, info_atual, campo_editado)
      values (auth.uid(), 'EdicaoDemanda - Alteracao', new.cd_demanda, coalesce(v_old, ''), coalesce(v_new, ''), v_col);
    end if;
  end loop;
  return new;
end;
$$;

create trigger audita_demandas
  after insert or update on demandas
  for each row execute function trg_audita_demandas();

-- Perfil criado automaticamente ao registrar usuário no Auth.
-- Novos usuários SEMPRE nascem como 'usuario' — promoção a admin
-- só por um administrador na tela de usuários.
create or replace function public.handle_new_user() returns trigger
language plpgsql security definer set search_path = public as $$
begin
  insert into public.usuarios (id, email, nome, perfil)
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data ->> 'nome', split_part(new.email, '@', 1)),
    'usuario'::perfil_usuario
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();

