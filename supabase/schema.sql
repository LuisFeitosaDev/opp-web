-- =====================================================================
-- USUÁRIOS, PERFIS E PERMISSÕES
-- =====================================================================

create type perfil_usuario as enum ('admin', 'usuario');

create table usuarios (
  id uuid primary key references auth.users (id) on delete cascade,
  nome text not null default '',
  email text not null unique,
  perfil perfil_usuario not null default 'usuario',
  bloqueado boolean not null default false,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now()
);

-- Catálogo de telas/módulos do sistema (permissões por tela)
create table telas (
  codigo text primary key,
  nome text not null,
  grupo text not null default 'Sistema',
  ordem integer not null default 0
);

create table permissoes (
  usuario_id uuid not null references usuarios (id) on delete cascade,
  tela_codigo text not null references telas (codigo) on delete cascade,
  pode_visualizar boolean not null default true,
  pode_editar boolean not null default false,
  primary key (usuario_id, tela_codigo)
);

-- Log de auditoria (mesmo modelo do SysPlan)
create table log_transacoes (
  cd_transacao bigint generated by default as identity primary key,
  usuario_id uuid references usuarios (id),
  transacao text not null,
  cd_item_transacao integer default 0,
  dt_transacao timestamptz not null default now(),
  info_anterior text,
  info_atual text,
  campo_editado text
);

create index idx_log_transacoes_item on log_transacoes (cd_item_transacao);
create index idx_log_transacoes_dt on log_transacoes (dt_transacao desc);
create index idx_log_transacoes_usuario on log_transacoes (usuario_id);
create index idx_log_transacoes_transacao on log_transacoes (transacao);

-- =====================================================================
