-- Usuários / permissões / telas
create policy usuarios_select on usuarios for select to authenticated using (true);
create policy usuarios_admin_all on usuarios for all to authenticated
  using (fn_is_admin()) with check (fn_is_admin());

create policy telas_select on telas for select to authenticated using (true);
create policy telas_admin on telas for all to authenticated
  using (fn_is_admin()) with check (fn_is_admin());

create policy permissoes_select_propria on permissoes for select to authenticated
  using (usuario_id = auth.uid() or fn_is_admin());
create policy permissoes_admin on permissoes for all to authenticated
  using (fn_is_admin()) with check (fn_is_admin());

-- Logs: leitura restrita à tela admin_logs; escrita via funções security definer
create policy log_admin_select on log_transacoes for select to authenticated
  using (fn_tem_permissao('admin_logs'));

-- PRMs espelhados: somente leitura para autenticados. NÃO há política de
-- escrita — nem admin altera pelo app; apenas o service_role (sync) grava.
create policy prm_grupo_sel on prm_grupo for select to authenticated using (true);
create policy prm_combos_sel on prm_combos for select to authenticated using (true);
create policy prm_gp_sel on prm_grupo_planejamento for select to authenticated using (true);

-- Demandas: leitura/escrita conforme permissão da tela
create policy demandas_sel on demandas for select to authenticated
  using (fn_tem_permissao('lista_demandas'));
create policy demandas_ins on demandas for insert to authenticated
  with check (fn_tem_permissao('lista_demandas', true));
create policy demandas_upd on demandas for update to authenticated
  using (fn_tem_permissao('lista_demandas', true));
-- Exclusão física não permitida (exclusão é lógica via status = 'EXCLUIDO')

create policy arquivos_sel on arquivos for select to authenticated
  using (fn_tem_permissao('lista_demandas'));
create policy arquivos_all on arquivos for all to authenticated
  using (fn_tem_permissao('lista_demandas', true))
  with check (fn_tem_permissao('lista_demandas', true));

